package com.matthew;

public class Dimensions {

    private int lenght;
    private int width;
    private int depth;

    public Dimensions(int lenght, int width, int depth) {
        this.lenght = lenght;
        this.width = width;
        this.depth = depth;
    }



    public int getLenght() {
        return lenght;
    }

    public int getWidth() {
        return width;
    }

    public int getDepth() {
        return depth;
    }
}
